import numpy as np
arr = np.array([1,2,3,4,5])
print("Nilai minimal = ", arr.min())
print("Nilai maksimal = ", arr.max())
print("Nilai rata-rata = ", arr.mean())
print("Total nilai = ", arr.sum())
print("Standar Deviasi = ", arr.std())
print("Index nilai minimal = ", arr.argmin())
print("Index nilai maksimal = ", arr.argmax())